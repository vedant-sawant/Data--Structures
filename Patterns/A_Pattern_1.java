public class A_Pattern_1
{
	public static void main(String args[])
	{
		for(int i=1;i<=3;i++)
		{
			for(int j=1;j<=3;j++)
			{
				System.out.print(" * ");
			}
			System.out.println();
		}

	}
}

/*    OUTPUT
123
Reverse of the number is:
321 
*/